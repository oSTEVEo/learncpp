int find_first(std::string s, char c) {
    for (int i = 0; i < s.length(); i++)
        if (s[i] == c)
            return i;
    return -1;
}

bool is_done(std::string s) {
    for (int i = 0; i < s.length(); i++)
        if (s[i] != '*')
            return false;
    return true;
}

int main() {
    using namespace std;

    cout << greeting << endl;

    const int N = 10;
    string word = magic_word;
    string display = "";
    for (int i = 0; i < word.length(); i++)
        display += '*';
    
    char letter;
    for (int i = 0; i < N; i++) {
        cout << display << "\t[Осталось " << N-i << " попыток]\tВведите букву: ";
        cin >> letter;
        int index = find_first(word, letter); 
        while (index+1) {
            display[index] = word[index];
            word[index] = '*';
            index = find_first(word, letter);
        }
        if (is_done(word)) {
            cout << "Вы выйграли! Число попыток: " << i << endl;
            return 0;
        }
    }
    cout << "Вы проиграли! Изначальное слово: " << magic_word << endl;
}